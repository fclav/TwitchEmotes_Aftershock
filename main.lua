local function remove_emotes(x, y, z)
  for i = #y, 1, -1 do
    for j = 1, #x do
      z[x[j]] = nil --lol     --remove from chat
      if y[i] == x[j] then
        table.remove(y, i) 		--remove from autocomplete suggestions
        break
      end
    end
  end
end

